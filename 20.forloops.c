#include <stdio.h>

int main(){
    // for loop 
    // for(initialization; condition; increment/decrement){arguments}
    
    // print 1 to 10
    for(int i =1 ; i<=10 ; i++){
        printf("%d \n",i);
    }

    // print 10 to 1
    for (int i=10; i>=1; i--){
        printf("%d \n",i);
    }


}
